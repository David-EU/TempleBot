#!/bin/sh
TOON=$1
REALM=$2
REGION=$3
DPS=$4
PAWN=$5
SIMCHTMLFILE=$6
DIRPATH=$7

RANDOMNAME=$(cat /dev/urandom | tr -cd 'a-f0-9'| head -c 32).html

cp $DIRPATH/TemplateP1.html $DIRPATH/$RANDOMNAME
sed -i "s/TOONREPLACEME/$TOON/g" $DIRPATH/$RANDOMNAME
sed -i "s/REALMREPLACEME/$REALM/g" $DIRPATH/$RANDOMNAME
sed -i "s/REGIONREPLACEME/$REGION/g" $DIRPATH/$RANDOMNAME
sed -i "s/DPSREPLACEME/$DPS/g" $DIRPATH/$RANDOMNAME
sed -i "s/PAWNREPLACEME/$PAWN/g" $DIRPATH/$RANDOMNAME
cat $SIMCHTMLFILE $DIRPATH/TemplateP2.html >> $DIRPATH/$RANDOMNAME

mv $DIRPATH/$RANDOMNAME $DIRPATH/sims/$RANDOMNAME
rsync -r $DIRPATH/sims/ USER@IPOFREMOTEMACHINE:FILELOCATIONFORWEB
echo $RANDOMNAME
